def checkForRelationEdges(currentInstance, expectedEdge, expectedNode):
    nodeFound = False
    # Get all of the outgoing properties for the instance
    for prop in currentInstance.get_properties():
        # Get the target of the outgoing property
        for propValue in prop[currentInstance]:
            # Check if the property contains the expected node as the target
            #nodeFound = re.search(expectedNode.lower() + r'\d+', str(propValue))
            nodeType = type(propValue)
            if isinstance(nodeType, ThingClass):
                if nodeType.name == expectedNode:
                    if prop.name == expectedEdge:
                        nodeFound = True
                else:
                    # Check if it's a subtype of the expected node type?
                    for isA in nodeType.ancestors():
                        if isA.name == expectedNode:
                            if prop.name == expectedEdge:
                                return True
                        else:
                            nodeFound = False
            else:
                nodeFound = False
            # If so, return true and print the property
            if nodeFound:
                # print(currentInstance, prop.python_name, propValue)
                return True
    # Get all of the incoming properties for the instance
    for inverseProp in currentInstance.get_inverse_properties():
        nodeFound = False
        # Get the source and value of the property
        source, value = inverseProp
        # Check if the property contains the expected node as the source
        # nodeFound = re.search(expectedNode.lower() + r'\d+', str(source))
        nodeType = type(source)
        if isinstance(nodeType, ThingClass):
            if nodeType.name == expectedNode:
                if inverseProp.name == expectedEdge:
                    nodeFound = True
            else:
                # Check if it's a subtype of the expected node type?
                for isA in nodeType.ancestors():
                    if isA.name == expectedNode:
                        if inverseProp.name == expectedEdge:
                            return True
                    else:
                        nodeFound = False
        else:
            nodeFound = False
        # If so, return true and print the property
        if nodeFound:
            # print(currentInstance, prop.python_name, propValue)
            return True
    # If nothing found, return False
    return False



