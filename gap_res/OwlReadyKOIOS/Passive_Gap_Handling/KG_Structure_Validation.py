def generateNodeSets():
    # Get the uninitalized ontology
    emptyGraph = get_ontology("http://localhost:3030/uagent").load()
    # Iterate through all of the classes in the ontology
    for ontoClass in emptyGraph.classes():
        linkedNodes = []
        edgesAndNodes = []
        # Iterate through all of the properties applied to the given class
        for property in ontoClass.get_class_properties():
            # Get the list of "targets" for the property
            propertyTargetList = property[ontoClass]
            # For each target, check if it's an ontology item as opposed to a string or boolean
            for propertyTarget in propertyTargetList:
                if isinstance(propertyTarget, ThingClass):
                    # Append just the target to the list of target nodes
                    linkedNodes.append(propertyTarget.name)
                    # Append both the name of the property and the target to the edge/node list
                    edgesAndNodes. append((property.name, propertyTarget.name))
                    #print(property[ontoClass])

        # Update the dictionaries for just nodes and node/edge combos
        if len(linkedNodes) > 0:
            autoGeneratedNodeSets.update({ontoClass.name: linkedNodes})
            autoGeneratedNodeEdgeSets.update({ontoClass.name: edgesAndNodes})

